;┌──────────────────────────────────────────────┐
;│ Retrieves the real part of a complex number	│
;│ to the destination string					│
;│ input:										│
;│			rcx = fa_complex					│
;│ output:										│
;│			xmm0 = real part of fa_complex		│
;└──────────────────────────────────────────────┘
proc fa_creal
	movsd	xmm0,QWORD [rcx]
	ret
endp